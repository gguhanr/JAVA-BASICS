import java.util.*;
class SwitchBreak{
    public static void main(String[] args){
        int n=1;
        switch(n){
            case 1:
            System.out.println("good ");
            break;
            case 2:
            System.out.println("morning");
            break;
            default:
            System.out.println("default case");
        }
    }
}