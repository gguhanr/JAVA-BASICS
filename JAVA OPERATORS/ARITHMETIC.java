class ARITHMETIC{
    public static void main(String[] args) {
        int a=10,b=3;

        int sum=a+b;
        int diff=a-b;
        int mul=a*b;
        int div=a/b;
        int mod=a%b;

        System.out.println("Sum "+sum);
        System.out.println("Difference "+diff);
        System.out.println("Multiplication "+mul);
        System.out.println("Moduls "+mod);
        System.out.println("division "+div);
    }
}